cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
  i <- x$getinverse()
  if(!is.null(i)) {
    message("getting cached data") ## if inverse is computed before , return it
    return(i)
  }
  data <- x$get()
  i <- solve(data, ...)  ##for computing inverse of the matrix
  x$setinverse(i)
  i
}
  
